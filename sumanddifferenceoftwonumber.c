#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
int a,b,sum,difference;	
    scanf("%d %d ",&a,&b);
    sum=a+b;
    difference=a-b;
    printf("%d %d\n",sum,difference);
float x,y,s,d;       
scanf("%f%f ",&x,&y);
    s=x+y;
    d=x-y;
printf("%0.1f %0.1f ",s,d);
    
 
    return 0;
}
